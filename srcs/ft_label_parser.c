#include "asm.h"

static int	ft_islabel(char *str)
{
	int 	i;

	if ((str[i] >= 97 && str[i] <= 122) || (str[i] >= 48 && str[i] <= 57)
			|| str[i] == '_')
		return (1);
	return (0);
}


